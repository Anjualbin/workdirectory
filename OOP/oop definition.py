# OOP Programming

#  class    : design pattern/blueprint
#  object   : real world entity to represent the class
#  reference: memory location to perform the actions on object


# self : instance keyword to represent object

class Person:
    def walk(self):
        print("walking")
    def read(self):
        print("reading")

mayukha=Person()
mayukha.walk()
mayukha.read()

obj2=Person()
obj2.walk()
obj2.read()


